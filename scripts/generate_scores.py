def main():
    with open(DATA_DIR / "occupations_raw.json") as f:
        data = json.load(f)

    scores = []
    for occ in data["occupations"]:
        code = occ["code"]
        middle = occ["middle"]

        # Get score: override > baseline
        if code in OVERRIDES:
            score = OVERRIDES[code]
        elif middle in MIDDLE_BASELINES:
            score = MIDDLE_BASELINES[middle]
        else:
            score = 5  # default fallback

        scores.append({
            "code": code,
            "name": occ["name"],
            "major": occ["major"],
            "middle": occ["middle"],
            "minor": occ["minor"],
            "score": score,
        })

    # Sort by score descending
    scores_sorted = sorted(scores, key=lambda x: (-x["score"], x["code"]))

    # Stats
    from collections import Counter
    score_dist = Counter(s["score"] for s in scores)
    avg_score = sum(s["score"] for s in scores) / len(scores)

    output = {
        "metadata": {
            "source": "Expert assessment based on AI/automation capability analysis",
            "methodology": "Middle category baseline + individual occupation overrides",
            "rubric": {
                "0-1": "几乎不可能被替代，需大量体力劳动或复杂物理环境",
                "2-3": "替代难度很高，需精细手工操作或特定物理环境",
                "4-5": "中等替代可能，部分可自动化但仍需人工参与",
                "6-7": "较高替代可能，大部分工作可由AI完成",
                "8-9": "非常高的替代可能，工作产出几乎完全数字化",
                "10": "几乎确定被替代，完全数字化且高度模式化",
            },
            "key_signal": "工作产出是否数字化，能否远程完成",
            "total_occupations": len(scores),
            "average_score": round(avg_score, 1),
            "score_distribution": {str(k): v for k, v in sorted(score_dist.items())},
        },
        "scores": scores_sorted,
    }

    output_path = DATA_DIR / "scores.json"
    with open(output_path, "w") as f:
        json.dump(output, f, ensure_ascii=False, indent=2)

    print(f"Scored {len(scores)} occupations")
    print(f"Average score: {avg_score:.1f}/10")
    print(f"\nScore distribution:")
    for s in range(11):
        count = score_dist.get(s, 0)
        bar = "█" * (count // 5)
        print(f"  {s:2d}: {count:4d} {bar}")

    print(f"\nTop 10 most replaceable:")
    for s in scores_sorted[:10]:
        print(f"  {s['score']}/10  {s['code']} {s['name']}")

    print(f"\nTop 10 least replaceable:")
    for s in scores_sorted[-10:]:
        print(f"  {s['score']}/10  {s['code']} {s['name']}")


if __name__ == "__main__":
    main()
